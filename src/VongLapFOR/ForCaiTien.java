package VongLapFOR;

public class ForCaiTien {
    public static void main(String[] args) {

        int array1[] = new int[]{5,10,15,20,25,30};

        //Khai bao FOR cai tien de DUYET mang/Array List
        for (int tenbien1 : array1){
            System.out.println(tenbien1);
        }
    }
}
